package day02_Variables_scanner_15KASIM;

public class C03_NonPrimitiveDataTypes {
    public static void main(String[] args) {


        String str= "Java candır";
        int sayi=10;

        // primitive data türlerinin hazır method'ları yoktur
        // ancak non-primitive data türlerinin önceden hazırlanmış
        // bizim kullanabileceğimiz method'lae olur

        System.out.println(sayi);// 10

        System.out.println(str .toLowerCase()); // java candir

        System.out.println(str); // Java candır

        System.out.println(str .toUpperCase());// JAVA CANDIR


        System.out.println(str); // Java candır

        System.out.println(str .isEmpty()); // false












    }
}
